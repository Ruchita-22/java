package com.ibm.model;

public class Employee {

	private String fname;
	private String lname;
	private String fthname;
	private String mthname;
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	public String getFthname() {
		return fthname;
	}
	public void setFthname(String fthname) {
		this.fthname = fthname;
	}
	public String getMthname() {
		return mthname;
	}
	public void setMthname(String mthname) {
		this.mthname = mthname;
	}
	
	
	
	
}
